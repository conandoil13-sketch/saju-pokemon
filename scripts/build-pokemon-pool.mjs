import { writeFile } from "node:fs/promises";

const POKEAPI_BASE_URL = "https://pokeapi.co/api/v2";
const POKEMON_POOL_LIMIT = 1025;
const POKEMON_FETCH_BATCH_SIZE = 20;
const REQUEST_TIMEOUT_MS = 15000;
const OUTPUT_PATH = new URL("../data/pokemon-pool.js", import.meta.url);

async function fetchJson(url) {
  const controller = new AbortController();
  const timeoutId = setTimeout(() => controller.abort(), REQUEST_TIMEOUT_MS);

  let response;
  try {
    response = await fetch(url, { signal: controller.signal });
  } finally {
    clearTimeout(timeoutId);
  }

  if (!response.ok) {
    throw new Error(`PokeAPI request failed: ${response.status} ${url}`);
  }

  return response.json();
}

function extractKoreanName(speciesPayload, fallbackName) {
  const koreanName = speciesPayload?.names?.find((entry) => entry.language?.name === "ko");
  return koreanName?.name ?? fallbackName;
}

function extractImageUrl(pokemonPayload) {
  return (
    pokemonPayload.sprites?.other?.["official-artwork"]?.front_default ??
    pokemonPayload.sprites?.other?.home?.front_default ??
    pokemonPayload.sprites?.front_default ??
    null
  );
}

async function fetchPokemonById(id) {
  const [pokemonPayload, speciesPayload] = await Promise.all([
    fetchJson(`${POKEAPI_BASE_URL}/pokemon/${id}`),
    fetchJson(`${POKEAPI_BASE_URL}/pokemon-species/${id}`),
  ]);
  const nameEn = pokemonPayload.name;

  return {
    id: pokemonPayload.id,
    nameEn,
    nameKo: extractKoreanName(speciesPayload, nameEn),
    types: pokemonPayload.types
      .sort((first, second) => first.slot - second.slot)
      .map((entry) => entry.type.name),
    imageUrl: extractImageUrl(pokemonPayload),
  };
}

async function buildPokemonPool() {
  const pokemonPool = [];
  let done = 0;

  for (let startId = 1; startId <= POKEMON_POOL_LIMIT; startId += POKEMON_FETCH_BATCH_SIZE) {
    const ids = Array.from(
      { length: Math.min(POKEMON_FETCH_BATCH_SIZE, POKEMON_POOL_LIMIT - startId + 1) },
      (_, index) => startId + index,
    );

    const results = await Promise.all(
      ids.map(async (id) => {
        try {
          return await fetchPokemonById(id);
        } catch (error) {
          console.warn(`Skipped Pokemon id ${id}:`, error.message);
          return null;
        } finally {
          done += 1;
        }
      }),
    );

    pokemonPool.push(...results.filter(Boolean));
    console.log(`Fetched ${done}/${POKEMON_POOL_LIMIT}`);
  }

  return pokemonPool;
}

function toModuleSource(pokemonPool) {
  return `// Generated by scripts/build-pokemon-pool.mjs.
// Keep this file committed so GitHub Pages can serve Pokemon data without live PokeAPI fan-out.
export const STATIC_POKEMON_POOL = ${JSON.stringify(pokemonPool, null, 2)};
`;
}

const pokemonPool = await buildPokemonPool();
if (pokemonPool.length === 0) {
  throw new Error("No Pokemon were fetched. Existing static pool was not overwritten.");
}
await writeFile(OUTPUT_PATH, toModuleSource(pokemonPool), "utf8");
console.log(`Wrote ${pokemonPool.length} Pokemon to ${OUTPUT_PATH.pathname}`);
